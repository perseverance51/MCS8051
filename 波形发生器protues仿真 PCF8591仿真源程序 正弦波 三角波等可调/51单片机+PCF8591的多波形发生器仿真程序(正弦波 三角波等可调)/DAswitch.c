extern uchar flag,time1;

void sin(void)	 //正弦波
{
    uint i;
    for(i=0; i<256; i++)
    {
        write_add(0x40,sin_tab[i]);	  //data byte
        //check_ACK();                  //检查应答位
        delay_1ms(time1);
    }
}

void main()
{
    uchar wave_select=0;
    init();
    while(1)
    {
        keyscan();
        if(Key5==0)
        {
            wave_select++;
            while(!Key5);
            if(wave_select==4)
            {
                wave_select=1;
            }
        }
        if(wave_select==1)
            triangular();
        if(wave_select==2)
            square();
        if(wave_select==3)
            sin();
    }

}





