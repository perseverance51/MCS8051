bit flag=0;

void delay(unsigned  int N)
{
    int i;
    for(i=0; i<N; i++);
}

void conversion_once_0832(unsigned char out_data)
{
    data_OUT=out_data; //输出数据
    delay(10);         //延时等待转换
}
uchar  keyscan()
{
    uchar key;
    if(P1!=0xff)
    {
        delay(61);
        key=P1;
        if(key!=0xff)
            flag=~flag;

    }
    return(key);
}

void triangle()
{
    uchar k;
    for(k=0; k<255; k++)
        conversion_once_0832(k);
    for(; k>0; k--)
        conversion_once_0832(k);
}
void square()
{
    conversion_once_0832(0xff);
    delay(1000);
    conversion_once_0832(0x00);
    delay(1000);

}
void juchibo()
{
    uchar j;
    for(j=0; j<255; j++)
        conversion_once_0832(j);
}
void sin()
{
    uchar i;
    for(i=0; i<255; i++)
    {
        conversion_once_0832(dat[i]);
    }
}
void main()
{
    uchar temp;
    while(1)
    {
        temp=keyscan();
        switch(temp)
        {
        case 0xfe:
            do {
                triangle();
            } while(keyscan()&&(flag==1));
            break;
        case 0xfd:
            do {
                square();
            } while(keyscan()&&(flag==1));
            break;
        case 0xfb:
            do {
                juchibo();
            } while(keyscan()&&(flag==1));
            break;
        case 0xf7:
            do {
                sin();
            } while(keyscan()&&(flag==1));
            break;
        default:
            break;
        }
    }

}